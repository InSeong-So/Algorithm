describe('01_올바른_괄호', () => {
  // 테스트 케이스명
  it('기본1', () => {
    // 파라미터 정의
    let a = '(()(()))(()';
    const result = 'NO';

    // 테스트 결과 정의
    expect(solution(a)).toEqual(result);
  });
});
