  // 테스트 케이스명
  it('기본1', () => {
    // 파라미터 정의
    let str = '(A(BC)D)EF(G(H)(IJ)K)LM(N)';
    const result = 'EFLM';

    // 테스트 결과 정의
    expect(solution(str)).toEqual(result);
  });
