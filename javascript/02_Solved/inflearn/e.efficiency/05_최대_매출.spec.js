describe('05_최대_매출', () => {
  // 테스트 케이스명
  it('기본1', () => {
    // 파라미터 정의
    let k = 3;
    let arr = [12, 15, 11, 20, 25, 10, 20, 19, 13, 15];
    const result = 56;

    // 테스트 결과 정의
    expect(solution(k, arr)).toEqual(result);
  });
});
