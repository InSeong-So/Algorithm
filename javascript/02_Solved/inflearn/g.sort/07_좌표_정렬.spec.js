describe('07_좌표_정렬', () => {
  // 테스트 케이스명
  it('기본1', () => {
    // 파라미터 정의
    let arr = [
      [2, 7],
      [1, 3],
      [1, 2],
      [2, 5],
      [3, 6],
    ];
    const result = [
      [1, 2],
      [1, 3],
      [2, 5],
      [2, 7],
      [3, 6],
    ];

    // 테스트 결과 정의
    expect(solution(arr)).toEqual(result);
  });
});
