# 체크: 점화식 다시 세우고 풀기
# 그리디 접근은 옳지 않다.
N = int(input())

dp = [0] * (N+1)
dp[0] = 0
dp[1] = 0
dp[2] = 1
dp[3] = 1

for i in range(4, N + 1):
  dp[i] = dp[i - 1] + 1

  if i % 2 == 0:
    dp[i] = min(dp[i // 2] + 1, dp[i])
  if i % 3 == 0:
    dp[i] = min(dp[i // 3] + 1, dp[i])

print(dp[N])

